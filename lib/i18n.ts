type TranslationTree = typeof translations.en;
type NestedKey<T, Prefix extends string = ""> = T extends string
  ? Prefix extends ""
    ? never
    : Prefix
  : T extends readonly string[]
    ? Prefix extends ""
      ? never
      : Prefix
    : {
        [K in keyof T]: K extends string
          ? NestedKey<T[K], Prefix extends "" ? K : `${Prefix}.${K}`>
          : never;
      }[keyof T];

export type TranslationKey = NestedKey<TranslationTree>;

function getNested(obj: Record<string, unknown>, path: string): string | undefined {
  const value = path.split(".").reduce<unknown>((acc, key) => {
    if (acc && typeof acc === "object" && key in acc) {
      return (acc as Record<string, unknown>)[key];
    }
    return undefined;
  }, obj);
  return typeof value === "string" ? value : undefined;
}

export function t(locale: Locale, key: TranslationKey): string {
  const bundle = translations[locale] ?? translations.en;
  return getNested(bundle as Record<string, unknown>, key) ?? key;
}

export function planLabel(locale: Locale, plan: PlanKey): string {
  return t(locale, `plans.${plan}` as TranslationKey);
}

